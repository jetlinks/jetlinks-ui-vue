<template>
    <div class="init-home-container">
        <div class="title">请选择首页视图</div>

        <div class="choose-view">
            <div class="view-content">
                <div
                    :span="8"
                    class="select-item"
                    v-for="item in list"
                    :key="item.id"
                    @click="selectValue = item.id"
                    :class="{
                        active: selectValue === item.id,
                    }"
                >
                    <div class="select-item-box">
                        <div class="select-title">{{ item.name }}</div>
                    </div>
                    <img
                        :src="getImage(`/home/home-view/${item.id}.png`)"
                        alt=""
                    />
                </div>
            </div>
            <div class="btn">
                <j-button type="primary" @click="confirm">保存修改</j-button>
            </div>
        </div>
    </div>
</template>

<script lang="ts" setup>
import { setView_api } from '@/api/home';
import { getImage } from '@/utils/comm';
import { useUserInfo } from '@/store/userInfo';

const list = [
    {
        id: 'device',
        name: '设备接入视图',
    },
    {
        id: 'ops',
        name: '运营管理视图',
    },
    {
        id: 'comprehensive',
        name: '综合管理视图',
    },
];

const user = useUserInfo();
const emits = defineEmits(['refresh']);
const selectValue = ref('device');

const confirm = () => {
    setView_api({
        name: 'view',
        content: selectValue.value,
    }).then(() => emits('refresh'));
};

watch(
    () => user.userInfos,
    (val: any) => {
        // 超管默认综合管理视图
        if (val.username === 'admin') {
            selectValue.value = 'comprehensive';
            confirm();
        }
    },
);
</script>

